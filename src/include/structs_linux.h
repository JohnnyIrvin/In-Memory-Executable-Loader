#include <stdint.h>

#include "macros.h"

#if defined(OS_LINUX)
#ifndef STRUCTS_LINUX_H
#define STRUCTS_LINUX_H

#if defined(BITS_32)
typedef struct elf_header_t {
    uint8_t e_ident[16];
    uint16_t e_type;
    uint16_t e_machine;
    uint32_t e_version;
    uint32_t e_entry;
    uint32_t e_phoff;
    uint32_t e_shoff;
    uint32_t e_flags;
    uint16_t e_ehsize;
    uint16_t e_phentsize;
    uint16_t e_phnum;
    uint16_t e_shentsize;
    uint16_t e_shnum;
    uint16_t e_shstrndx;
} ELF_HEADER, *PELF_HEADER;
#elif defined(BITS_64)
typedef struct elf_header_t {
    uint8_t e_ident[16];
    uint16_t e_type;
    uint16_t e_machine;
    uint32_t e_version;
    uint64_t e_entry;
    uint64_t e_phoff;
    uint64_t e_shoff;
    uint32_t e_flags;
    uint16_t e_ehsize;
    uint16_t e_phentsize;
    uint16_t e_phnum;
    uint16_t e_shentsize;
    uint16_t e_shnum;
    uint16_t e_shstrndx;
} ELF_HEADER, *PELF_HEADER;
#endif

#endif // STRUCTS_LINUX_H
#endif // OS_LINUX